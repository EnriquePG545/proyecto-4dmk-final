create or replace function public.fn_actualizar_updated_at()
returns trigger
language plpgsql
set search_path = public
as $$
begin
    new.updated_at = now();
    return new;
end;
$$;

create or replace function public.fn_es_admin()
returns boolean
language sql
security definer
set search_path = public
as $$
    select exists (
        select 1
        from public.admins
        where admins.user_id = auth.uid()
    );
$$;

alter table public.admins
add column if not exists rol text not null default 'admin';

do $$
begin
    if not exists (
        select 1
        from pg_constraint
        where conname = 'admins_rol_check'
          and conrelid = 'public.admins'::regclass
    ) then
        alter table public.admins
        add constraint admins_rol_check
        check (rol in ('propietario', 'admin', 'trabajador', 'lectura'))
        not valid;
    end if;

    begin
        alter table public.admins validate constraint admins_rol_check;
    exception
        when others then null;
    end;
end;
$$;

