create or replace function public.fn_registrar_venta_sistema(
    p_id_cliente bigint,
    p_id_producto bigint,
    p_id_metodo_pago bigint,
    p_cantidad integer,
    p_fecha_venta date,
    p_observacion text default null
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
    v_stock integer;
    v_precio numeric(12, 2);
    v_id_venta bigint;
begin
    if not public.fn_es_admin() then
        raise exception 'No autorizado';
    end if;

    if p_cantidad is null or p_cantidad <= 0 then
        raise exception 'La cantidad debe ser mayor a cero';
    end if;

    select stock, precio
    into v_stock, v_precio
    from public.sistema_productos
    where id_producto = p_id_producto
    for update;

    if not found then
        raise exception 'Producto no encontrado';
    end if;

    if v_stock < p_cantidad then
        raise exception 'No hay suficiente stock para realizar esta venta';
    end if;

    update public.sistema_productos
    set stock = stock - p_cantidad
    where id_producto = p_id_producto;

    insert into public.sistema_ventas (
        id_cliente,
        id_producto,
        id_metodo_pago,
        cantidad,
        precio_unitario,
        fecha_venta,
        observacion
    )
    values (
        p_id_cliente,
        p_id_producto,
        p_id_metodo_pago,
        p_cantidad,
        v_precio,
        coalesce(p_fecha_venta, current_date),
        p_observacion
    )
    returning id_venta into v_id_venta;

    return v_id_venta;
end;
$$;

create or replace function public.fn_anular_venta_sistema(p_id_venta bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
    v_venta public.sistema_ventas%rowtype;
begin
    if not public.fn_es_admin() then
        raise exception 'No autorizado';
    end if;

    select *
    into v_venta
    from public.sistema_ventas
    where id_venta = p_id_venta
    for update;

    if not found then
        raise exception 'Venta no encontrada';
    end if;

    if v_venta.anulada then
        raise exception 'La venta ya esta anulada';
    end if;

    update public.sistema_ventas
    set anulada = true
    where id_venta = p_id_venta;

    update public.sistema_productos
    set stock = stock + v_venta.cantidad
    where id_producto = v_venta.id_producto;
end;
$$;

do $$
declare
    item record;
begin
    for item in
        select table_name
        from information_schema.tables
        where table_schema = 'public'
          and table_name in (
            'sistema_clientes',
            'sistema_productos',
            'sistema_ventas',
            'sistema_configuracion'
          )
    loop
        execute format('drop trigger if exists trg_%I_updated_at on public.%I', item.table_name, item.table_name);
        execute format('create trigger trg_%I_updated_at before update on public.%I for each row execute function public.fn_actualizar_updated_at()', item.table_name, item.table_name);
    end loop;
end;
$$;

