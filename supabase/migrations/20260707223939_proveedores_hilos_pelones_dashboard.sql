create table if not exists public.proveedores (
    codigo_tienda text primary key,
    nombre_tienda text not null,
    direccion_proveedor text not null default '-',
    telefono text not null default '-',
    observacion text,
    activo boolean not null default true,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint proveedores_codigo_tienda_check check (btrim(codigo_tienda) <> ''),
    constraint proveedores_nombre_tienda_check check (btrim(nombre_tienda) <> '')
);

drop trigger if exists trg_proveedores_updated_at on public.proveedores;

create trigger trg_proveedores_updated_at
before update on public.proveedores
for each row
execute function public.fn_actualizar_updated_at();

