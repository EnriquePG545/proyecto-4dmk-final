create table if not exists public.compras_hilos (
    id_compra_hilo bigint generated by default as identity primary key,
    id_hilo bigint references public.inventario_hilos(id_hilo) on delete set null,
    codigo_hilo_snapshot text not null,
    nombre_color_snapshot text not null,
    codigo_tienda text not null references public.proveedores(codigo_tienda) on update cascade on delete restrict,
    cantidad integer not null,
    precio_unitario numeric(12, 2) not null,
    total_compra numeric(12, 2) generated always as (cantidad::numeric * precio_unitario) stored,
    fecha_compra date not null default current_date,
    detalle_compra text,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint compras_hilos_cantidad_check check (cantidad > 0),
    constraint compras_hilos_precio_unitario_check check (precio_unitario >= 0)
);

